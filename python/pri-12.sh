#!/bin/bash

# 사용자로부터 파일 경로 입력받기
echo "분석할 개인정보처리방침 파일의 경로를 입력하세요:"
read filepath

# 파일 존재 여부 확인
if [ ! -f "$filepath" ]; then
    echo "파일을 찾을 수 없습니다: $filepath"
    exit 1
fi

# 제12조 정보 추출
echo "제12조(개인정보 처리방침의 변경에 관한 사항)"
echo "========================================"

# 제12조의 내용을 출력
awk '/제12조\(개인정보 처리방침의 변경에 관한 사항\)/,/제13조/' "$filepath" | grep -v "제13조"

echo "========================================"
