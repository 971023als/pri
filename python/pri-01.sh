#!/bin/bash

# 사용자로부터 파일 경로 입력받기
echo "분석할 개인정보처리방침 파일의 경로를 입력하세요:"
read filepath

# 파일 존재 여부 확인
if [ ! -f "$filepath" ]; then
    echo "파일을 찾을 수 없습니다: $filepath"
    exit 1
fi

# 제1조와 제2조 정보 추출
echo "개인정보 처리 목적과 보유 기간:"
echo "========================================"

# 제1조 추출
echo "제1조(개인정보의 처리목적)"
awk '/제1조\(개인정보의 처리목적\)/,/제2조/' "$filepath" | grep -v "제2조"

# 제2조 추출
echo "제2조(개인정보의 처리 및 보유기간)"
awk '/제2조\(개인정보의 처리 및 보유기간\)/,EOF' "$filepath" | grep -v "제3조"

echo "========================================"
