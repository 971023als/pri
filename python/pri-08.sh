#!/bin/bash

# 사용자로부터 파일 경로 입력받기
echo "분석할 개인정보처리방침 파일의 경로를 입력하세요:"
read filepath

# 파일 존재 여부 확인
if [ ! -f "$filepath" ]; then
    echo "파일을 찾을 수 없습니다: $filepath"
    exit 1
fi

# 제8조 정보 추출
echo "제8조(개인정보의 안전성 확보조치에 관한 사항)"
echo "========================================"

# 제8조의 내용을 출력
awk '/제8조\(개인정보의 안전성 확보조치에 관한 사항\)/,/제9조/' "$filepath" | grep -v "제9조"

echo "========================================"
