#!/bin/bash

# 사용자로부터 파일 경로 입력받기
echo "분석할 개인정보처리방침 파일의 경로를 입력하세요:"
read filepath

# 파일 존재 여부 확인
if [ ! -f "$filepath" ]; then
    echo "파일을 찾을 수 없습니다: $filepath"
    exit 1
fi

# 제3조 정보 추출
echo "제3조(처리하는 개인정보의 항목)"
echo "========================================"

# 개인정보 항목, 운영 근거, 보유기간 등을 추출
awk '/제3조\(처리하는 개인정보의 항목\)/,/제4조/' "$filepath" | grep -v "제4조"

echo "========================================"
